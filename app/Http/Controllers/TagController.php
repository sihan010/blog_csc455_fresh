    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $tag=Tag::find();
        $tag->posts()->detach();
        $tag->delete();
        Session::flash('done','Tag was Successfully Destroyed');

        return redirect()->route('tags.index');
    }
}
